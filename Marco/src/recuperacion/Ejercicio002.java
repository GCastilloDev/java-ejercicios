    public void calcular (int num){
        
        String mensaje = "";
        
        if(num >=0 ){
            mensaje = "El número " + num + " es positivo";
        }
        else{
            mensaje = "El número " + num + " es negativo";
        }
        
        lblResultado.setText(mensaje);
    }

