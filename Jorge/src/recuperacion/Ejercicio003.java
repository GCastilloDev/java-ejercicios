    public void seleccionarTipoTexto (){
        
        
        if (rbtNormal.isSelected()){
            lblResultado.setFont(new java.awt.Font("Roboto", 0, 15));
        }
        
        if (rbtNegrita.isSelected()){
         lblResultado.setFont(new java.awt.Font("Roboto", 1, 15));
        }
        
        if (rbtCursiva.isSelected()){
            lblResultado.setFont(new java.awt.Font("Roboto", 2, 15));
        }
        
        if (rbtNegritaItalica.isSelected()){
            lblResultado.setFont(new java.awt.Font("Roboto", 3, 15));
        }
        
    }

