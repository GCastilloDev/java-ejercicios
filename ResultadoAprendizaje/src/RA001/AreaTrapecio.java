    private void txtBaseMayorKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBaseMayorKeyTyped
        
        char tecla;
        tecla = evt.getKeyChar();
        String cod = txtBaseMayor.getText();
        int punto = cod.indexOf(".")+1;
        
        if (punto==0){
            if (!Character.isDigit(tecla)&&tecla!=KeyEvent.VK_BACKSPACE&&tecla!=KeyEvent.VK_PERIOD){
                evt.consume();
                getToolkit().beep();
            }
        }
        
        else{
            if (!Character.isDigit(tecla)&&tecla!=KeyEvent.VK_BACKSPACE){
                evt.consume();
                getToolkit().beep();
            }
        }
    }//GEN-LAST:event_txtBaseMayorKeyTyped

